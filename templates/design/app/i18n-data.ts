type Messages = typeof enUS;
type DeepPartial<T> = {
  [K in keyof T]?: T[K] extends Record<string, unknown>
    ? DeepPartial<T[K]>
    : T[K];
};

type PartialMessages = DeepPartial<Messages>;

function isRecord(value: unknown): value is Record<string, unknown> {
  return !!value && typeof value === "object" && !Array.isArray(value);
}

function deepMerge<T extends Record<string, unknown>>(
  base: T,
  overrides: DeepPartial<T> | undefined,
): T {
  if (!overrides) return base;
  const next = { ...base } as Record<string, unknown>;
  for (const [key, value] of Object.entries(overrides)) {
    const baseValue = next[key];
    next[key] =
      isRecord(baseValue) && isRecord(value)
        ? deepMerge(baseValue, value as DeepPartial<typeof baseValue>)
        : value;
  }
  return next as T;
}

function mergeMessages(overrides: PartialMessages): Messages {
  return deepMerge(enUS, overrides);
}

function mergePartialMessages(
  ...overrides: PartialMessages[]
): PartialMessages {
  return overrides.reduce<PartialMessages>(
    (messages, override) => deepMerge(messages, override),
    {},
  );
}

